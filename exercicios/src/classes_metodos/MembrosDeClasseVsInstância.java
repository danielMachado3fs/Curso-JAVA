package classes_metodos;

public class MembrosDeClasseVsInstância {
	
	/*
	 * <<MEMBROS DE CLASSE>>
	 * 
	 * Membros podem ser definidos como Métodos e Atributos.
	 * 
	 * Os membros de classe são aqules que pertencem a classe, ou seja, todas as instâncias(objetos)
	 * criadas pela classe, o atributo(membro) que pertence aquela classe, terá o mesmo valor em todas
	 * as instâncias.
	 * As intâncias poderam acessar o valor mas será somente uma cópia, não poderá mudálo.
	 * 
	 * OBS: Os membros das classes são definidos pela palavra STATIC na frente do nome.
	 * 
	 * <<MEMBROS DE INSTÂNCIA>>
	 * Os membros de instância são aqueles que pertencem às instâncias, ou seja,
	 * em cada intância pode ser definido um valor diferente para aquele membro.
	 * 
	 * EXEMPLO
	 * 
	 * Em uma classe chamada "Área Da Circunferência", é definido o membro chamado PI = 3,14,
	 * isso quer dizer que em todas as instâncias o valor de PI será 3,14 sem a necessidade de 
	 * armazenar esse valor em um espaço de memória diferente para cada instância criada.
	 * 
	 * OBS: Os membros státicos, ou seja, que pertencem às classes, podem ser acessados
	 * diretamente das classes com a notação ponto, além de poderem ser acessádos 
	 * também pelas instâncias(O que não é o recomendado)0:
	 * 
	 * 		NomeDaClasse.NomeDoMembro
	 * 
	 * Já os membros que pertence aos objetos, só podem ser acessados a partir do momento
	 * que uma instância é criada:
	 * 
	 * 		NomeDaIntamcia.NomeDoMembro
	 */

}
