package classes_metodos;

public class AtribuicaoValorVsReferencias {
	
	/*
	 * <<Atribuir valor a uma VARIÁVEL do tipo primitiva>>
	 * 
	 * Ao atribuir valor a uma variável do tipo primitiva, é separado um espaço de memória
	 * como o rótulo igual o nome da variável e dentro desse espaço é armazenado o VALOR.
	 * 
	 * <<Atribuir valores a um OBJETO>>
	 * 
	 * Ao atribuir valores para um objeto, é separado um espaço na memória com o rótulo
	 * igual o nome do objeto e dentro desse espaço vai ser armazenado uma REFERENCIA
	 * apontando para outro espaço de meória onde realmente estão os dados do objeto.
	 * Ao atribuir os valores de um objeto para outro objeto, será criado um outro espaço na
	 * memória com rótulo igual o nome do objeto e dentro dele a mesma referencia apontando
	 * para os mesmos dados do outro objeto.
	 * 
	 * EXEMPLO:
	 * Data d1 = new Data(); //Criação de um objeto dada chamado "d1"
	 * Data d2 = d1; //Atribuição dos valores de d1 para d2.
	 * 
	 * >>>GARBAGE COLLECTION<<<
	 * É um "coletor de lixo do java" que exclui os objetos que não estão sendo referenciados
	 * na memória, ou seja, a aplicação nã está mais usando aquele objeto.
	 * 
	 * 
	 *
	 */

}
