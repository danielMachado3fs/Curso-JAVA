package classes_metodos;

public class ClassesVsObjetos {
	
	/*
	 * >>REVISAO<<
	 * **Uma classe pública ou seja "public class" deverá OBRIGATÓRIAMENTE ter o mesmo nome do arquivo .java
	 * **Uma classe define um bloco de código
	 * 
	 * <<CLASSES>>
	 * **O Método MAIN é um método para executar os códigos do arquivo
	 * ____________________________________________________
	 * 
	 * **SINTAXE: declaração de uma classe
	 * 
	 *  	[modificadores] class (nome) [variações]{
	 *  
	 *  		corpo = membros (atributos e comportamentos)	
	 *  
	 *  	}
	 *  
	 *  ___________________________________________________
	 *  
	 *  Atributos = dados
	 *  Comportamentos = métodos
	 *  
	 *  **Uma classe define um tipo de estrutura de dados personalizado
	 *  **Uma classe representa uma abstração(simplificação)
	 *  **A partir da classe criamos objetos e neles colocamos os dados. Por exemplo:
	 *  Criamos uma classe "Produto", nessa clase o que vai variar é os dados de 
	 *  cada produto, e cada produto será um objeto(instancia)
	 *  
	 *  Membros de uma classe:
	 *  *Variáveis/Constantes	 *  	
	 *  *Métodos
	 *  *Construtores (métodos especiais)
	 *  *Classes
	 *  
	 *  **Para acessar os membros de um objeto/classe utiliza-se a notação ponto
	 *  SINTAXE:
	 *  
	 *  	nomeClasse/Objeto.nomeMembro;
	 *  
	 *  
	 * 
	 *
	 */

}
